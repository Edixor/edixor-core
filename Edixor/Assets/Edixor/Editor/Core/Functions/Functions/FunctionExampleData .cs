
public class FunctionExampleData 

{
    public string name;
    public string container;
    public string path;
    public string type;
    public string action;
    public bool isActive;

    public FunctionExampleData(string name, string container, string path, string type, string action, bool isActive)
    {
        this.name = name;
        this.container = container;
        this.path = path;
        this.type = type;
        this.action = action;
        this.isActive = isActive;
    }
}